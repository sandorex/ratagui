    /// Maps ratatui color to egui, None only if color is `Color::Reset`
    pub fn map_color(&self, color: ratatui_core::style::Color) -> Option<Color32> {
        use ratatui_core::style::Color as RColor;
        match color {
            RColor::Reset => None,
            RColor::Black => Some(self.black()),
            RColor::Red => Some(self.red()),
            RColor::Green => Some(self.green()),
            RColor::Yellow => Some(self.yellow()),
            RColor::Blue => Some(self.blue()),
            RColor::Magenta => Some(self.magenta()),
            RColor::Cyan => Some(self.cyan()),
            RColor::White => Some(self.white()),
            RColor::LightGreen => Some(self.bright_green()),
            RColor::LightYellow => Some(self.bright_yellow()),
            RColor::LightRed => Some(self.bright_red()),
            RColor::LightCyan => Some(self.bright_cyan()),
            RColor::LightBlue => Some(self.bright_blue()),
            RColor::LightMagenta => Some(self.bright_magenta()),
            RColor::Gray => Some(self.bright_white()),
            RColor::DarkGray => Some(self.bright_black()),
            RColor::Rgb(r, g, b) => Some(egui::Color32::from_rgb(r, g, b)),
            RColor::Indexed(i) => Some(self.color(i)),
        }
    }
